public class Pudding extends Food {

    {
        image = ItemSpriteSheet.PUDDING;
        energy = 400f;
        clean = 250f;
    }

    @Override
    public int value() {
        return 100 * quantity;
    }

    @Override
    public void execute( Hero hero, String action ) {

        super.execute( hero, action );

        if (action.equals( AC_EAT )) {

            Sample.INSTANCE.play( Assets.Sounds.PUDDING );

        }
    }
}
